/**
 * OperatorIncrementdanDecrement16
 */
public class OperatorIncrementdanDecrement16 {
    public static void main(String[] args) {
        int i = 4;
       //increment
       System.out.println("i : " + i);
       System.out.println("++i : " + ++i);
       System.out.println("i++ : " + i++);
       //decrement
       System.out.println("--i : " + i);
       System.out.println("i-- : " + i--);
       System.out.println("i++ : " + i);
    }
    }
